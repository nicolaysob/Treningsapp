function isActive(pathname: string, href: string) {
  if (href === "/") return pathname === "/";
  return pathname.startsWith(href);
}

function isStandaloneMode(): boolean {
  if (typeof window === "undefined") return false;
  return (
    ("standalone" in navigator && (navigator as Navigator & { standalone?: boolean }).standalone) ||
    window.matchMedia("(display-mode: standalone)").matches
  );
}

export function BottomNav() {
  const pathname = usePathname();
  const [pendingFriends, setPendingFriends] = useState(0);
  const [mounted, setMounted] = useState(false);
  const [standalone, setStandalone] = useState(false);

  useEffect(() => {
    setMounted(true);
    setStandalone(isStandaloneMode());
  }, []);

  useEffect(() => {
    fetch("/api/friends/pending-count")
      .then((res) => res.json())
      .then((data: { count?: number }) => setPendingFriends(data.count ?? 0))
      .catch(() => setPendingFriends(0));
  }, [pathname]);

  const nav = (
    <nav className="bottom-nav" aria-label="Hovedmeny">
      <div className="bottom-nav__inner">
        <div className="flex items-stretch justify-around px-0.5 py-1">
          {NAV_ITEMS.map((item) => {
            const active = isActive(pathname, item.href);
            return (
              <Link
                key={item.href}
                href={item.href}
                prefetch={false}
                scroll={false}
                className={`bottom-nav__item relative ${active ? "nav-item-active text-[#ff6b2b]" : "text-zinc-600"}`}
              >
                <span className="relative">
                  {item.icon}
                  {item.href === "/friends" && pendingFriends > 0 && (
                    <span className="nav-badge" aria-label={`${pendingFriends} ventende forespørsler`}>
                      {pendingFriends > 9 ? "9+" : pendingFriends}
                    </span>
                  )}
                </span>
                <span className={`text-[7px] font-bold tracking-wide sm:text-[8px] ${active ? "text-[#ff6b2b]" : ""}`}>
                  {item.label}
                </span>
              </Link>
            );
          })}
        </div>
      </div>
    </nav>
  );

  if (!mounted) return null;

  // Safari-fane: fast portal. Hjemskjerm-PWA: flex-rad inni app-shell.
  if (standalone) return nav;
  return createPortal(nav, document.body);
}
